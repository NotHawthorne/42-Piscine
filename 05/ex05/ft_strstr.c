char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int b;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			b = 0;
			while (str[i + b] == to_find[b] && str[i + b] != '\0')
			{
				if (to_find[b + 1] == '\0')
					return (&str[i]);
				b++;
			}
		}
		i++;
	}
	return (0);
}
