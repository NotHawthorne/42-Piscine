char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		str[i] = str[i] >= 'a' && str[i] <= 'z' ? str[i] - 32 : str[i];
		i++;
	}
	return (str);
}
