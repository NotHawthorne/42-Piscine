char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (src[i] != '\0')
	{
		dest[b] = src[i];
		b++;
		i++;
	}
	dest[b] = '\0';
	return (dest);
}
