int			ft_any(char **tab, int (*f)(char*))
{
	static int i[2];

	while (tab[i[0]])
		i[1] += (*f)(tab[i[0]++]) == 1 ? 1 : 0;
	return (i[1] = i[1] > 0 ? 1 : 0);
}
